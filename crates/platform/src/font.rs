//! Built-in 8×8 monochrome bitmap font for real glyph rasterization.
//!
//! This module is gated behind the `sixel` feature (it is only needed by the
//! Sixel/Kitty graphics backend in `sixel.rs`). Each glyph is an
//! `[u8; 8]` — 8 rows of 8 bits, MSB = leftmost pixel. Glyphs are best-effort
//! block patterns, distinguishable but not typographically perfect.

#![cfg(feature = "sixel")]

/// Glyph width in pixels.
pub const GLYPH_W: usize = 8;
/// Glyph height in pixels.
pub const GLYPH_H: usize = 8;

/// Return the 8×8 bitmap for `ch`, or `None` if the character is unsupported.
#[must_use]
pub fn glyph(ch: char) -> Option<[u8; 8]> {
    GLYPHS.iter().find(|(k, _)| **k == ch).map(|(_, g)| *g)
}

