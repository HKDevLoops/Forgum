#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn shell_parse_valid() {
        assert_eq!(Shell::parse("bash"), Some(Shell::Bash));
        assert_eq!(Shell::parse("zsh"), Some(Shell::Zsh));
        assert_eq!(Shell::parse("fish"), Some(Shell::Fish));
        assert_eq!(Shell::parse("pwsh"), Some(Shell::Pwsh));
        assert_eq!(Shell::parse("powershell"), Some(Shell::PowerShell));
        assert_eq!(Shell::parse("cmd"), Some(Shell::Cmd));
    }

    #[test]
    fn shell_parse_invalid() {
        assert_eq!(Shell::parse("ksh"), None);
        assert_eq!(Shell::parse(""), None);
    }

    #[test]
    fn bash_hook_has_required_parts() {
        let hook = generate_hook(Shell::Bash, "/usr/local/bin/forgum-engine");
        assert!(hook.contains("forgum()"));
        assert!(hook.contains("__forgum_precmd"));
        assert!(hook.contains("PROMPT_COMMAND"));
        assert!(hook.contains("/usr/local/bin/forgum-engine"));
        assert!(hook.contains("--background"));
        assert!(hook.contains("--duration 0"));
    }

    #[test]
    fn zsh_hook_uses_precmd_functions() {
        let hook = generate_hook(Shell::Zsh, "/usr/local/bin/forgum-engine");
        assert!(hook.contains("precmd_functions"));
        assert!(hook.contains("forgum()"));
    }

    #[test]
    fn fish_hook_uses_function() {
        let hook = generate_hook(Shell::Fish, "/usr/local/bin/forgum-engine");
        assert!(hook.contains("function forgum"));
        assert!(hook.contains("function __forgum_sweep"));
        assert!(hook.contains("--on-event fish_prompt"));
    }

    #[test]
    fn pwsh_hook_has_prompt_wrapper() {
        let hook = generate_hook(Shell::Pwsh, "C:\\forgum-engine.exe");
        assert!(hook.contains("function forgum"));
        assert!(hook.contains("__ForgumPromptBackup"));
        assert!(hook.contains("function global:prompt"));
    }

    #[test]
    fn cmd_hook_has_required_parts() {
        let engine = "C:\\forgum-engine.exe";
        let hook = generate_cmd_hook(engine);
        assert!(hook.contains(">>> forgum"));
        assert!(hook.contains("<<< forgum"));
        assert!(hook.contains("prompt"));
        assert!(hook.contains("sweep"));
        assert!(hook.contains(engine));
        assert!(hook.contains("cmd"));
    }

    #[test]
    fn powershell_hook_has_prompt_wrapper() {
        let hook = generate_hook(Shell::PowerShell, "C:\\forgum-engine.exe");
        assert!(hook.contains("function forgum"));
        assert!(hook.contains("__ForgumPromptBackup"));
        assert!(hook.contains("function global:prompt"));
    }

    #[test]
    fn cmd_display_and_parse() {
        assert_eq!(Shell::Cmd.to_string(), "cmd");
        assert_eq!(Shell::PowerShell.to_string(), "powershell");
        assert_eq!(Shell::parse("CMD"), Some(Shell::Cmd));
    }

    #[test]
    fn all_hooks_contain_engine_path() {
        let path = "/opt/forgum/bin/forgum-engine";
        for shell in [
            Shell::Bash,
            Shell::Zsh,
            Shell::Fish,
            Shell::Pwsh,
            Shell::Cmd,
            Shell::PowerShell,
        ] {
            let hook = generate_hook(shell, path);
            assert!(hook.contains(path), "shell {shell} missing engine path");
        }
    }

    #[test]
    fn all_hooks_have_header_and_footer() {
        for shell in [
            Shell::Bash,
            Shell::Zsh,
            Shell::Fish,
            Shell::Pwsh,
            Shell::Cmd,
            Shell::PowerShell,
        ] {
            let hook = generate_hook(shell, "/usr/bin/forgum-engine");
            assert!(hook.contains(">>> forgum"), "shell {shell} missing header");
            assert!(hook.contains("<<< forgum"), "shell {shell} missing footer");
        }
    }

    #[test]
    fn zellij_config_has_required_parts() {
        let config = generate_zellij_config("/usr/local/bin/forgum-engine");
        assert!(config.contains(">>> forgum zellij >>>"));
        assert!(config.contains("<<< forgum zellij <<<"));
    }

    #[test]
    fn wezterm_config_has_required_parts() {
        let config = generate_wezterm_config("/usr/local/bin/forgum-engine");
        assert!(config.contains(">>> forgum wezterm >>>"));
        assert!(config.contains("<<< forgum wezterm <<<"));
        assert!(config.contains("/usr/local/bin/forgum-engine"));
    }

    #[test]
    fn screen_config_has_required_parts() {
        let config = generate_screen_config("/usr/local/bin/forgum-engine");
        assert!(config.contains(">>> forgum screen >>>"));
        assert!(config.contains("<<< forgum screen <<<"));
    }

    #[test]
    fn tmux_config_has_required_parts() {
        let config = generate_tmux_config("/usr/local/bin/forgum-engine");
        assert!(config.contains(">>> forgum tmux >>>"));
        assert!(config.contains("<<< forgum tmux <<<"));
        assert!(config.contains("status-right-length 100"));
        assert!(config.contains("status-interval 5"));
        assert!(config.contains("status-right"));
        assert!(config.contains("status-line --max-len 70"));
        assert!(config.contains("pane-focus-in"));
        assert!(config.contains("pane-focus-out"));
        assert!(config.contains("/usr/local/bin/forgum-engine"));
    }
}
