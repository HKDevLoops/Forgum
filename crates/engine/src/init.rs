fn generate_fish_hook(engine: &str) -> String {
    format!(
        r#"# >>> forgum (fish) >>>
# Generated by forgum-engine init fish — do not edit by hand
set -g __forgum_engine "{engine}"
set -g __forgum_config "$FORGUM_CONFIG"; or set -g __forgum_config "$HOME/.config/Forgum/config.json"
set -g __forgum_runtime (set -q XDG_RUNTIME_DIR; and echo "$XDG_RUNTIME_DIR/forgum"; or echo "/tmp/forgum-"(id -u)"/forgum")
mkdir -p $__forgum_runtime

function forgum
    set text $argv
    if test (count $argv) -eq 0
        set text ($__forgum_engine fortune 2>/dev/null)
    end
    $__forgum_engine render --config $__forgum_config --text "$text" --background --duration 0 $argv
end

function __forgum_sweep --on-event fish_prompt
    set state $__forgum_runtime/daemon.json
    if test -f $state
        set pid (awk -F'"' '/pid/{{print $4}}' $state 2>/dev/null)
        if test -n "$pid"; and not kill -0 $pid 2>/dev/null
            set rows (awk -F'"' '/ob_y1/{{print $4}}' $state 2>/dev/null; or echo 0)
            set cols $COLUMNS
            printf '\x1b7'
            for y in (seq 1 $rows); printf '\x1b[%d;1H%*s' $y $cols ''; end
            printf '\x1b8\x1b[0m'
            rm -f $state
        end
    end
end
# <<< forgum <<<
"#
    )
}

fn generate_pwsh_hook(engine: &str) -> String {
    format!(
        r#"# >>> forgum (pwsh) >>>
# Generated by forgum-engine init pwsh — do not edit by hand
$__ForgumEngine = '{engine}'
$__ForgumConfig = if ($env:FORGUM_CONFIG) {{ $env:FORGUM_CONFIG }} else {{ Join-Path $env:APPDATA 'Forgum\config.json' }}

function forgum {{
    param([Parameter(ValueFromRemainingArguments)][string[]]$Args)
    $text = if ($Args) {{ ($Args -join ' ') }} else {{ & $__ForgumEngine fortune 2>$null }}
    & $__ForgumEngine render --config $__ForgumConfig --text $text --background --duration 0 @Args
}}

$global:__ForgumPromptBackup = $function:prompt
function global:prompt {{
    $state = Join-Path $env:TEMP 'Forgum\daemon.json'
    if (Test-Path $state) {{
        $info = Get-Content $state -Raw | ConvertFrom-Json
        if ($info.pid -and -not (Get-Process -Id $info.pid -EA SilentlyContinue)) {{
            [Console]::Write("`e7")
            1..$info.ob_y1 | ForEach-Object {{ [Console]::Write("`e[$($_);1H$(' ' * ($Host.UI.RawUI.WindowSize.Width)))") }}
            [Console]::Write("`e8`e[0m")
            Remove-Item $state -Force
        }}
    }}
    & $global:__ForgumPromptBackup
}}
# <<< forgum <<<
"#
    )
}

fn generate_cmd_hook(engine: &str) -> String {
    format!(
        r#"@echo off
rem >>> forgum (cmd) >>>
rem Generated by forgum-engine init cmd — do not edit by hand
rem
rem NOTE: cmd.exe has NO precmd hook model. We inject a `prompt` macro that
rem runs the dead-daemon sweeper on every prompt, and register a `forgum`
rem alias via doskey. The previous prompt is preserved by capturing it into
rem _FORGUM_OLD_PROMPT first. To persist across sessions, either source this
rem file from your auto-run script or set the registry AutoRun key:
rem   reg add "HKCU\Software\Microsoft\Command Processor" /v AutoRun /t REG_SZ /d "<this-file>"
rem (AutoRun persistence is user-opt-in; forgum never mutates the registry.)
set "_FORGUM_ENGINE={engine}"
if not defined _FORGUM_OLD_PROMPT set "_FORGUM_OLD_PROMPT=$P$G"
doskey forgum="{engine}" $*
prompt $_FORGUM_OLD_PROMPT$G & "%_FORGUM_ENGINE%" sweep
rem <<< forgum <<<
"#
    )
}

