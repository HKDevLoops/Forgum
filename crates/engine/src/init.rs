//! Shell hook generation for `forgum init <shell>`.
//!
//! Generates shell-specific hooks that:
//! 1. Resolve the engine path (baked at init time)
//! 2. Call `forgum-engine render` with argv flags (no JSON in shell)
//! 3. Sweep dead daemons on precmd
//!
//! Supported shells: bash, zsh, fish, pwsh (7+), powershell.exe (5.1), cmd.exe.

/// Supported shell types. Canonical definition lives in `forgum-platform::shell`;
/// re-exported here so engine code keeps using `forgum_engine::init::Shell`.
pub use forgum_platform::shell::Shell;

/// Generate a shell hook script.
///
/// `engine_path` is the absolute path to `forgum-engine`, baked in at init time.
pub fn generate_hook(shell: Shell, engine_path: &str) -> String {
    match shell {
        Shell::Bash => generate_bash_hook(engine_path),
        Shell::Zsh => generate_zsh_hook(engine_path),
        Shell::Fish => generate_fish_hook(engine_path),
        Shell::Pwsh => generate_pwsh_hook(engine_path),
        Shell::Cmd => generate_cmd_hook(engine_path),
        Shell::PowerShell => generate_powershell_hook(engine_path),
    }
}

/// Generate a tmux config block for `forgum tmux install`.
///
/// `engine_path` is the absolute path to `forgum-engine`, baked in at init time.
pub fn generate_tmux_config(engine_path: &str) -> String {
    format!(
        "# >>> forgum tmux >>>\n\
         # Generated by forgum-engine tmux install — do not edit by hand\n\
         set -g status-right-length 100\n\
         set -g status-interval 5\n\
         set -g status-right \"#({engine} status-line --max-len 70 2>/dev/null)\"\n\
         set-hook -g pane-focus-in  'run-shell \"{engine} --background --daemon 2>/dev/null\"'\n\
         set-hook -g pane-focus-out 'run-shell \"{engine} control send stop 2>/dev/null\"'\n\
         # <<< forgum tmux <<<\n",
        engine = engine_path
    )
}

pub fn generate_zellij_config(_engine_path: &str) -> String {
    "# >>> forgum zellij >>>\n\
     # Generated by forgum-engine zellij install\n\
     zellij run -- forgum-engine --background --daemon\n\
     # <<< forgum zellij <<<\n"
        .to_string()
}

pub fn generate_wezterm_config(engine_path: &str) -> String {
    format!(
        "-- >>> forgum wezterm >>>\n\
         -- Generated by forgum-engine wezterm install\n\
         local wezterm = require 'wezterm'\n\
         return {{\n\
         status_update_interval = 1000,\n\
         wezterm.on('update-status', function(window, pane)\n\
         local handle = io.popen('{engine} status-line --max-len 60 2>/dev/null')\n\
         local status = handle:read('*a') or ''\n\
         handle:close()\n\
         return {{ {{ Text = status }} }}\n\
         end),\n\
         }}\n\
         -- <<< forgum wezterm <<<\n",
        engine = engine_path
    )
}

pub fn generate_screen_config(engine_path: &str) -> String {
    format!(
        "# >>> forgum screen >>>\n\
         # Generated by forgum-engine screen install\n\
         # Add to .screenrc:\n\
         # {engine} --background --daemon\n\
         # <<< forgum screen <<<\n",
        engine = engine_path
    )
}

fn generate_bash_hook(engine: &str) -> String {
    format!(
        r#"# >>> forgum (bash) >>>
# Generated by forgum-engine init bash — do not edit by hand
__FORGUM_ENGINE="{engine}"
__FORGUM_CONFIG="${{FORGUM_CONFIG:-$HOME/.config/Forgum/config.json}}"
__FORGUM_RUNTIME="${{XDG_RUNTIME_DIR:-/tmp/forgum-$UID}}/forgum"
mkdir -p "$__FORGUM_RUNTIME"

forgum() {{
  local text="$*"
  if [ -z "$text" ]; then
    text="$("$__FORGUM_ENGINE" fortune 2>/dev/null)"
  fi
  "$__FORGUM_ENGINE" render \
    --config "$__FORGUM_CONFIG" \
    --text "$text" \
    --background --duration 0 "$@"
}}

__forgum_precmd() {{
  local state="$__FORGUM_RUNTIME/daemon.json"
  if [ -f "$state" ]; then
    local pid; pid=$(awk -F'"' '/pid/{{print $4}}' "$state" 2>/dev/null)
    if [ -n "$pid" ] && ! kill -0 "$pid" 2>/dev/null; then
      local rows; rows=$(awk -F'"' '/ob_y1/{{print $4}}' "$state" 2>/dev/null || echo 0)
      local cols=${{COLUMNS:-80}}
      printf '\x1b7'
      local y=1; while [ "$y" -le "$rows" ]; do printf '\x1b[%d;1H%*s' "$y" "$cols" ''; y=$((y+1)); done
      printf '\x1b8\x1b[0m'
      rm -f "$state"
    fi
  fi
}}
PROMPT_COMMAND="__forgum_precmd${{PROMPT_COMMAND:+; $PROMPT_COMMAND}}"
# <<< forgum <<<
"#
    )
}

